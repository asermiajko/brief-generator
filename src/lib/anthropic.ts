export async function generateBrief(
  wizardData: WizardData
): Promise<BriefResult> {
  const formattedData = formatWizardDataForPrompt(wizardData);

  const systemPrompt = `Ты — эксперт по созданию сайтов для жилых комплексов и застройщиков. У тебя опыт анализа 150+ сайтов ЖК разных классов жилья (эконом, комфорт, бизнес, премиум).

Твоя задача — на основе данных от клиента сгенерировать профессиональный бриф на разработку сайта ЖК. Бриф должен быть конкретным, практичным и готовым к передаче подрядчику.

Бриф должен содержать РОВНО 5 разделов. Каждый раздел должен быть подробным и содержать конкретные рекомендации, калиброванные под указанный класс жилья и тип сайта.

Формат ответа — строго JSON:
{
  "sections": [
    { "title": "Рекомендуемая структура сайта", "content": "..." },
    { "title": "Функционал (Must Have / Nice to Have)", "content": "..." },
    { "title": "Чеклист материалов для старта", "content": "..." },
    { "title": "Дизайн-концепция", "content": "..." },
    { "title": "CTA-стратегия и конверсионные элементы", "content": "..." }
  ]
}

Содержание каждого раздела — в формате Markdown (заголовки, списки, таблицы).

Требования к каждому разделу:

РАЗДЕЛ 1 — Рекомендуемая структура сайта:
- Список страниц с кратким описанием назначения каждой
- Калибровка под класс жилья, тип сайта и приоритет
- Для сайта застройщика: доп. блоки каталога ЖК с фильтрацией

РАЗДЕЛ 2 — Функционал (Must Have / Nice to Have):
- Список функций с пометками приоритетности
- Калибровка под класс жилья (например: 3D-тур — must для премиума, nice-to-have для эконома)
- Примеры: шахматка, интерактивный генплан, ипотечный калькулятор, фиды в агрегаторы, онлайн-бронирование, ход строительства

РАЗДЕЛ 3 — Чеклист материалов для старта:
- Что подготовить, в каком формате, зачем нужно
- Учёт того, что у клиента уже есть (из данных визарда)
- Приоритизация: без чего нельзя стартовать / что можно добавить позже

РАЗДЕЛ 4 — Дизайн-концепция:
- Сводка из ответов по ToV и визуальному стилю
- AI-рекомендации: стиль, палитра, типографика — калиброванные под класс жилья
- Текстовые описания референсов из базы сайтов ЖК

РАЗДЕЛ 5 — CTA-стратегия и конверсионные элементы:
- Какие формы, квизы, попапы, кнопки разместить
- Привязка к страницам из раздела 1 (где именно на сайте)
- Обоснование: почему именно эти элементы влияют на конверсию для данного класса жилья`;

  try {
    const message = await client.messages.create({
      model: "claude-sonnet-4-20250514",
      max_tokens: 8000,
      messages: [
        {
          role: "user",
          content: `Сгенерируй профессиональный бриф на разработку сайта ЖК на основе следующих данных:\n\n${formattedData}`,
        },
      ],
      system: systemPrompt,
    });

    // Extract text content
    const textBlock = message.content.find((block) => block.type === "text");
    if (!textBlock || textBlock.type !== "text") {
      throw new Error("AI не вернул текстовый ответ");
    }

    // Parse JSON from response
    const jsonMatch = textBlock.text.match(/\{[\s\S]*\}/);
    if (!jsonMatch) {
      throw new Error("AI вернул некорректный формат");
    }

    let parsed: { sections: { title: string; content: string }[] };
    try {
      parsed = JSON.parse(jsonMatch[0]);
    } catch {
      throw new Error("AI вернул невалидный JSON. Попробуйте ещё раз.");
    }

    if (!parsed.sections || !Array.isArray(parsed.sections)) {
      throw new Error("AI вернул некорректную структуру брифа");
    }

    return {
      sections: parsed.sections,
      generatedAt: new Date().toISOString(),
    };
  } catch (error: unknown) {
    // Fallback to mock if API returns 403 or any authentication error
    const err = error as { status?: number; message?: string };
    if (err.status === 403 || err.status === 401 || (err.message && err.message.includes("403"))) {
      console.warn("Anthropic API returned 403/401 — using mock brief result as fallback");
      return getMockBriefResult(wizardData);
    }
    throw error;
  }
}
