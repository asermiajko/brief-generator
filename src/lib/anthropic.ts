import Anthropic from "@anthropic-ai/sdk";
import {
  WizardData,
  HOUSING_CLASS_LABELS,
  SITE_TYPE_LABELS,
  SITE_GOAL_LABELS,
  TOV_SCALE_LABELS,
  COLOR_SCHEME_LABELS,
  BriefResult,
} from "@/types/wizard";

const client = new Anthropic({
  apiKey: process.env.ANTHROPIC_API_KEY!,
});

function formatWizardDataForPrompt(data: WizardData): string {
  const tov = TOV_SCALE_LABELS.map(
    (scale) =>
      `  - ${scale.left} ↔ ${scale.right}: ${data.toneOfVoice[scale.key]}/10`
  ).join("\n");

  const materials = [
    data.materials.renders && "рендеры",
    data.materials.photos && "фотографии",
    data.materials.layouts && "планировки",
    data.materials.genplan && "генплан",
    data.materials.brandbook && "брендбук",
  ]
    .filter(Boolean)
    .join(", ") || "ничего не отмечено";

  const refs =
    data.references.length > 0
      ? data.references
          .map((r) => `  - ${r.url} (${r.liked ? "нравится" : "не нравится"})`)
          .join("\n")
      : "  не указаны";

  return `
ДАННЫЕ ОТ КЛИЕНТА:

1. Класс жилья: ${data.housingClass ? HOUSING_CLASS_LABELS[data.housingClass] : "не выбран"}
2. Тип сайта: ${data.siteType ? SITE_TYPE_LABELS[data.siteType] : "не выбран"}
3. Цель сайта: ${data.siteGoal ? SITE_GOAL_LABELS[data.siteGoal] : "не выбрана"}

4. Tone of Voice:
${tov}

5. Визуальный стиль:
  - Композиция (простая ↔ динамичная): ${data.visualStyle.composition}/10
  - Типографика (без засечек ↔ с засечками): ${data.visualStyle.typography}/10
  - Формы (прямые ↔ закруглённые): ${data.visualStyle.shapes}/10
  - Цветовая схема: ${COLOR_SCHEME_LABELS[data.visualStyle.colorScheme]}

6. Имеющиеся материалы: ${materials}

7. УТП проекта: ${data.utp.text || "не указано"}

8. Референсы:
${refs}
`.trim();
}

